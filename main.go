// ArticlesFormData 创建 或者 编辑 博文表单数据
type ArticlesFormData struct {
	Title, Body string
	URL         *url.URL
	Errors      map[string]string
}

var router *mux.Router
var db *sql.DB

// Article  对应一条文章数据
type Article struct {
	Title, Body string
	ID          int64
}

// Link 方法用来生成文章链接， 给Article结构体添加方法
func (a Article) Link() string {
	showURL, err := router.Get("articles.show").URL("id", strconv.FormatInt(a.ID, 10))
	if err != nil {
		logger.LogError(err)
		return ""
	}
	return showURL.String()
}

// Delete 方法用以从数据库中删除单条记录
func (a Article) Delete() (rowsAffected int64, err error) {
	rs, err := db.Exec("DELETE FROM articles WHERE id = " + strconv.FormatInt(a.ID, 10))

	if err != nil {
		return 0, err
	}

	// √ 删除成功，跳转到文章详情页
	if n, _ := rs.RowsAffected(); n > 0 {
		return n, nil
	}

	return 0, nil
}

func articlesIndexHandler(w http.ResponseWriter, r *http.Request) {

	// 本方法的内容解析，请参考： https://learnku.com/courses/go-basic/1.17/article-list/11512
	// 1. 执行一个查询语句，返回一个结果集
	rows, err := db.Query("SELECT * FROM articles")
	logger.LogError(err)   // 要放在下一句之前执行，
	defer rows.Close()     // 1.在下方 rows.Next() 读到最后一条数据时，被执行， 2. 需在检测 err 以后调用，否则会让运行时 panic
	var articles []Article // map 类型，存放的是  Article 结构体

	// 2. 循环读取结果
	for rows.Next() {
		// 循环读取数据中，如果出错，会自动关闭数据库连接
		var article Article
		// 2.1 扫描每一行的结果并赋值到一个 article 对象中
		err := rows.Scan(&article.ID, &article.Title, &article.Body)
		logger.LogError(err)
		// 2.2 将article结构体追到 articles 的这个切片中
		articles = append(articles, article)
	}
	// 2.3 检测遍历时是否发生错误
	err = rows.Err()
	logger.LogError(err)

	// 3. 加载模板
	tmpl, err := template.ParseFiles("resources/views/articles/index.gohtml")
	logger.LogError(err)

	// 4. 渲染模板，将所有的文章的数据传输进去
	err = tmpl.Execute(w, articles) // 传的是个 map 类型
	logger.LogError(err)
}

func articlesStoreHandler(w http.ResponseWriter, r *http.Request) {
	title := r.PostFormValue("title")
	body := r.PostFormValue("body")
	errors := validateArticleFormData(title, body)
	// 检查是否有错误
	if len(errors) == 0 {
		lastInsertID, err := saveArticleToDB(title, body)
		if lastInsertID > 0 {
			fmt.Fprint(w, "插入成功，ID 为"+strconv.FormatInt(lastInsertID, 10))
		} else {
			logger.LogError(err)
			w.WriteHeader(http.StatusInternalServerError)
			fmt.Fprint(w, "500 服务器内部错误")
		}
	} else {

		storeURL, _ := router.Get("articles.store").URL()
		data := ArticlesFormData{
			Title:  title,
			Body:   body,
			URL:    storeURL,
			Errors: errors,
		}
		tmpl, err := template.ParseFiles("resources/views/articles/create.gohtml")
		if err != nil {
			panic(err)
		}

		err = tmpl.Execute(w, data)
		if err != nil {
			panic(err)
		}
	}
}

